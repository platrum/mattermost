export function localizeTimezoneLabel(label: string, locale: string): string {
    if (!locale.toLowerCase().startsWith('ru')) {
        return label;
    }

    return RU_TIMEZONE_LABELS[label] || label;
}

export function getLocalizedTimezoneLabel(timezones: Timezone[], timezone: string, locale: string): string {
    return localizeTimezoneLabel(getTimezoneLabel(timezones, timezone), locale);
}
